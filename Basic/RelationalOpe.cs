using System;

class RelationalOpe
{
    public static void Main(string[] args)
    {
        // Relational / comperesion operators = >, >=, <, <=, ==, !=
        
        int number1 = 6;
        int number2 = 4;

        Console.WriteLine(number1 > number2);
        Console.WriteLine(number1 >= number2);
        Console.WriteLine(number1 < number2);
        Console.WriteLine(number1 <= number2);
        Console.WriteLine(number1 == number2);
        Console.WriteLine(number1 != number2);

    }
}